def add_suffix(s):
    """
    Add sufix 's'
    """
    return "Tosia " + s

def add_prefix(s):
    """
    Add prefix 's'
    """
    return s + " Tosia"

def add_both(s):
    """
    Add prefix and suffix 's'
    """
    return s + " Samosia " + s

